/***
특정 제품을 기간내에(2010,12,01~2010.12.10) 구매한 고객들의 정보 추출
***/

SELECT *
FROM MEM
WHERE MEM_NO IN(SELECT CUSTOMERID
						FROM SALES
						WHERE STOCKCODE IN('21730','21615')
						AND INVOICEDATE BETWEEN '2010-12-01' AND '2010-12-10'
						)


/***
특정제품을 기간내에 구매하지 않은 고객들의 정보 추출
***/

SELECT *
FROM MEM
WHERE MEM_NO NOT IN(SELECT CUSTOMERID
						FROM SALES
						WHERE STOCKCODE IN('21730','21615')
						AND INVOICEDATE BETWEEN '2010-12-01' AND '2010-12-10'
						)


